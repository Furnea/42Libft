#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*c;
	size_t	i;

	i = 0;
	c = (char *)malloc(ft_strlen(s) + 1);
	if (c == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		c[i] = s[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
